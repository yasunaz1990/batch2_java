public class PrimitiveDataIntro {

    public static void main(String[] args) {

        // Primitive Data Types
        //----------------------------------
        //   1) Built into the Java language
        //   2) Foundation of all other types
        //

        // Integer Data Type
        //    byte  ----> -128 ~0~ +127    1b   0 ~100
        //    short ----> -32K ~0~ +32K    8b
        //    int   ----> -2B  ~0~ +2B     16b
        //    long  ----> -9GT ~0~ +9GT    64b
        byte score = 80;
        short feetInAMile = 5283;
        int milesToSun = 9299999;    // Use this
        long debt = 1811111012L;


        // Decimal Data Type
        // float
        // double
        float miles = 89234.0F;
        double money = 23434232.09;   // Use this


        // Characters Data Type
        char letter = 'A';
        char grad = 'F';
        char hashTag = '#';

        // Logic
        boolean isRaining = false;
        boolean isCold = true;
        boolean isJavaFun = true;




        // Reference Data Type
        // String



    }
}
