package lecture_03_operators;

public class RelationalOperator {

    public static void main(String[] args) {
        // Relational Operator
        // [Num] [r-ops] [Num] ----> [boolean data]
        // 100  is less than 30 ----> No
        // 100  <  30  ---> false
        //
        // List of relational operator
        //    >     greater than
        //    >=    greater than or equal to
        //    <     less than
        //    <=    less than or equal to
        //    ==    equal to
        //    !=    not equal to

        boolean ret = 11 != 11;
        System.out.println(ret);

    }
}
